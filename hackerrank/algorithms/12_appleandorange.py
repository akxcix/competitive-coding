# Complete the countApplesAndOranges function below.
def countApplesAndOranges(s, t, a, b, apples, oranges):
    m,n=0,0
    for apple in apples:
        if a+apple <=t and a+apple>=s:
            m+=1
    for orange in oranges:
        if b+orange<=t and b+orange >=s:
            n+=1
    print(m)
    print(n)
    

